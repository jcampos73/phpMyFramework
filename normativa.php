				<h1 class="mgtop20">Normativa de publicación de anuncios profesionales en stc motor</h1>
				<p class="mgtop10">Con la finalidad de dar a conocer la normativa que rige la publicación de anuncios en stc motor, a continuación se detalla la tipología de los anuncios que pueden publicarse en el portal y aquellos que no pueden ser aceptados con el objetivo de mantener la calidad del portal.</p>
				<h6 class="mgtop20 nigro">ANUNCIOS CORRECTOS</h6>
				<p>Los siguientes anuncios se consideran correctos para publicar en stc motor</p>
				<blockquote><span>· Anuncios de venta de vehículos o accesorios de las categorías de stc motor que no incumplan ninguna condición para ser considerados anuncios no aceptados.</span></blockquote>
				<h6 class="mgtop20 nigro">ANUNCIOS INCORRECTOS</h6>
				<p>Los siguientes tipos de anuncios <strong>no pueden publicarse</strong> en stc motor y si se publican serán  eliminados por el Dpto. de Calidad de stc motor</p>
				<h6 class="nigro punt">1.1 Anuncios de compra de vehículos</h6>
				<p>No se aceptan anuncios de demanda o compra de vehículos.</p>
				<h6 class="nigro punt">1.2 Anuncios de intercambio de vehículos</h6>
				<p>No se aceptan anuncios de intercambio de vehículos o partes de éstos.</p>
				<h6 class="nigro punt">1.3 Anuncios duplicados</h6>
				<p>No se aceptan anuncios duplicados de un mismo vehículo o artículo.</p>  
				<p>Ejemplo: Dos anuncios de la venta del mismo motor o varios anuncios del mismo vehículo.</p>
				<h6 class="nigro punt">1.4 Anuncios de motocicletas y quads</h6>
				<p>No se aceptan anuncios de motocicletas, quads o accesorios de motos (cascos, chaquetas, etc...)</p>
				<h6 class="nigro punt">1.5 Anuncios no relacionados con vehículos</h6>
				<p>No se aceptan anuncios por ejemplo de venta de pisos, parkings, entradas para conciertos o carreras, etc...</p>
				<h6 class="nigro punt">1.6 Anuncios que expresan opiniones o hacen consultas</h6>
				<p>No se aceptan anuncios tipo foro o blog que expresen opiniones o hagan consultas por ejemplo &#8220;Necesito encontrar modelo&#8230;..&#8221;</p>
				<h6 class="nigro punt">1.7 Anuncios con fotografías que no corresponden con el vehículo </h6>
				<p>No se aceptan anuncios cuyas fotografía no se correspondan con la marca y modelo / accesorios que se están anunciando, así como aquellas fotografías que incluyan personas.</p>
				<p>Tampoco se aceptan anuncios cuyas fotografías muestren instalaciones, campas, oficinas y donde no  aparezca explícitamente y de forma clara el vehículo / accesorio anunciado.</p>
				<h6 class="nigro punt">1.8 Anuncios con fotografías con números de teléfono, dirección, URL o dirección de correo electrónico.</h6>
				<p>No se aceptan anuncios cuyas fotografías incluyan números de teléfono, dirección o direcciones de Internet (dirección web o dirección correo electrónico). </p>
				<h6 class="nigro punt">1.9 Anuncios con fotografías que sean logotipos de compañías. </h6>
				<p>No se aceptan anuncios cuyas fotografías sean logotipos o presenten nombres de compañías, aunque sean los propios del anunciante. </p>
				<h6 class="nigro punt">1.10 Anuncios con fotografías con la marca de agua de otros portales </h6>
				<p>No se aceptan anuncios cuyas fotos incluyan marcas de agua (logotipos de fondo) de otros portales. </p>
				<h6 class="nigro punt">1.11 Anuncios con fotografías con marcos y otros retoques </h6>
				<p>No se aceptan anuncios cuyas fotos incluyan marcos o cualquier otro tipo de retoque. La foto debe ser únicamente del vehículo / accesorio anunciado.</p>
				<h6 class="nigro punt">1.12 Anuncios con palabras malsonantes o insultos </h6>
				<p>No se aceptan anuncios con insultos o palabras malsonantes.</p>
				<h6 class="nigro punt">1.13 Anuncios de venta y/o fabricación de réplicas </h6>
				<p>No se aceptan anuncios de réplicas de vehículos.</p>
				<h6 class="nigro punt">1.14 Anuncios incompletos o completados erróneamente</h6>
				<p>No se aceptan anuncios que no se hayan completado correctamente.</p>
				<p>Ejemplo: Nº de teléfono u otros datos que no correspondan en campos como Garantía, Color o Más Información.</p> 
				<h6 class="nigro punt">1.15 Anuncios donde el año del vehículo /accesorio no se corresponde</h6>
				<p>No se aceptan anuncios que llamen la atención porque no se corresponda el año con la gama del vehículo / accesorio. </p>
				<p>Ejemplo: Anuncio de venta de un Seat 600 que indique que es del año 2008.</p>
				<h6 class="nigro punt">1.16 Anuncios falsos</h6>
				<p>No se aceptan anuncios falsos o que induzcan a error de interpretación.</p>
				<p>Ejemplo: Que el precio indicado no se corresponda con el valor real o se publiquen mensualidades o plazos de pago en el campo Precio.</p>
				<h6 class="nigro punt">1.17 Anuncios con títulos erróneos</h6>
				<p>No se aceptan anuncios con títulos erróneos.</p>    
				<blockquote>
					<h6><i>Título correcto</i></h6>
					<p>El título correcto corresponde a la marca y modelo del vehículo que se vende o al nombre del accesorio o pieza que se vende.</p>
					<p>Se admiten los signos relativos a pulgadas para las llantas de los vehículos (") o cualquier otro signo que forme parte del modelo de un vehículo o pieza.</p>
					<h6 class="mgtop10"><i>Títulos no aceptados</i></h6>
					<p>Cualquier palabra o símbolo que no corresponda a una Marca y Modelo en las categorías de vehículos, o a un accesorio o pieza en la categoría Accesorios. Así mismo los títulos no podrán estar escritos en mayúsculas.</p> 
					<p>Ejemplos de palabras o símbolos no aceptados:</p>
			
					<p class="mgtop10">·	<strong>Verbos:</strong> Alquiler, Comprar, Vender, Vende, Vendo, Alquilo, Alquila, Compro, Compra, Venta, Compra, Alquila, Permuta, Trueque, Arriendo, Arrendar, Arrenda, y similares.</p>
			
					<p class="mgtop10">·	<strong>Adjetivos relativos al Precio:</strong> Barata, Barato, Chollo, Económica, Económico, Ganga, Ocasión,  Oferta, Ofertón, Oportunidad, Rebaja, Rebajado, Chollazo, Liquidación y similares.</p>
			
					<p class="mgtop10">·	<strong>Adverbios de Exclamación:</strong> Atención, mejor, urge, urgente y similares.</p>
			
					<p class="mgtop10">·	<strong>Adjetivos relativos al Aspecto:</strong> Bonita, Bonito, Chulo, Coqueta, Coqueto, Espectacular, Estupenda, Estupendo, Excelente, Excepcional, Extraordinaria, Extraordinario, Fabulosa, Fabuloso, Fantástica, Fantástico, Impecable, Impresionante, Increíble, Inmejorable, Lujo, Magnífica, Magnífico, Maravillosa, Maravilloso, Preciosa, Precioso, Cuco, Divino, Preciosísimo, Bombón, Bombonera, Descomunal, Sorprendente, Asombroso, Asombrosa, Lindo, Linda, Caprichoso, Caprichosa y similares.</p>
			
					<p class="mgtop10">·	<strong>Signos de admiración, figurativos tipo:</strong> ¡¡¡¡, ***** y similares.</p>
				   </blockquote>
			
				<h6 class="nigro punt">1.18 Anuncios con texto adicional incorrecto</h6>
				<p>No se aceptan anuncios con el texto adiconal incorrecto.</p>    
				<blockquote>
					<h6><i>Información adicional correcta</i></h6>
					<p>La información adicional correcta es la que aporta más información sobre el accesorio que se está anunciando.</p>
					<h6 class="mgtop10"><i>Textos no aceptados</i></h6>
					<p>Cualquier información adicional que no aporte información importante para la descripción del accesorio. Texto adicional escrito íntegramente en mayúsculas</p> 
					<p>Ejemplos de información no permitida:</p>
			
					<p class="mgtop10">·	<strong>Datos del venderor:</strong> No se pueden insertar datos de contacto tipo, mail, teléfono, dirección web, etc. Estos datos ya están disponibles en la zona de contacto de la ficha.</p>
			
					<p class="mgtop10">·	<strong>Idiopma:</strong> Solo se acpetarán anuncios que estén redactados en cualquiera de los idicomas oficiales del territorio español.</p>
				 </blockquote>
